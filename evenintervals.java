package aa;

public class evenintervals {
	public static void main(String args){
		{
		int i,j,k;
		for(i=10;i<100;i++) 
		{
		         k=0;
		         for(j=2;j<i;j++)
		         {
		                  if(i%j==0)
		                  k++;
		         }
		         System.out.print(i+"   ");
		}
		}
		}
}

